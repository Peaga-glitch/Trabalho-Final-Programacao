package modelo;

/**
 * Enumeração que padroniza os géneros cinematográficos disponíveis no catálogo do cinema.
 * * @author Teu Nome
 * @version 1.0
 */
public enum Genero {
    /** Filmes focados em ação, combates e perseguições. */
    ACAO, 
    /** Filmes focados em humor e entretenimento leve. */
    COMEDIA, 
    /** Obras focadas em arcos dramáticos intensos e narrativas profundas. */
    DRAMA, 
    /** Filmes focados em suspense, sustos e terror. */
    TERROR, 
    /** Filmes de ficção científica, tecnologia futurista ou fantasia espacial. */
    FICCAO_CIENTIFICA, 
    /** Obras focadas em relacionamentos amorosos e romance. */
    ROMANCE; 
}